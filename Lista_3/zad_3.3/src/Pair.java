public class Pair {
    private double cheated;
    private double nonCheated;

    public Pair(double cheated, double nonCheated) {
        this.cheated = cheated;
        this.nonCheated = nonCheated;
    }

    public double getCheated() {
        return cheated;
    }

    public double getNonCheated() {
        return nonCheated;
    }
}
