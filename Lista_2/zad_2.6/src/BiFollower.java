public class BiFollower {
    private String word;
    private String occurrences;

    public BiFollower(String word, String occurrences) {
        this.word = word;
        this.occurrences = occurrences;
    }

    public String getWord() {
        return word;
    }

    public String getOccurrences() {
        return occurrences;
    }
}
